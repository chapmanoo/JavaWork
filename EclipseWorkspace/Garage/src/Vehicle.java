
public class Vehicle {
	String make;
	String model;
	int yearMade;
	boolean hasWheels;
	String type;

	public Vehicle(String make, String model, int yearMade, boolean hasWheels, String type) {
		this.make = make;
		this.model = model;
		this.yearMade = yearMade;
		this.hasWheels = hasWheels;
		this.type = type;
	}
}
